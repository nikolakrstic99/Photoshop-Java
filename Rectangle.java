package photoshop;

public class Rectangle {
	private int row,column,width,height;

	public Rectangle(int row, int column, int width, int height) {
		super();
		this.row = row;
		this.column = column;
		this.width = width;
		this.height = height;
	}

	public int getRow() {
		return row;
	}

	public int getColumn() {
		return column;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
	
	
}
